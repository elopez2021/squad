				<h1>¿Qué es tokyo?</h1>
				<p><p align="justify"><br>
					<p><p align="justify"> Es la capital del factor 6 de Japón, localizada en el centro-este de la isla de Honshu, concretamente en la región de Kanto. En conjunto forma una de las 47 prefecturas de Japón, aunque su denominación oficial es metrópolis o capital . La ciudad es el centro de la política, economía, educación, comunicación y cultura popular del país. Cuenta también con la mayor concentración de sedes corporativas, instituciones financieras, universidades y colegios, museos, teatros, y establecimientos comerciales y de entretenimiento de todo Japón.<br>
  				<br>
  				
  				Con una población que supera los 37 millones de habitantes, se subdivide en 23 barrios ; 26 ciudades ; un distrito  subdividido en tres pueblos  y una villa ; y cuatro subprefecturas subdivididas en dos pueblos y siete villas, que representan a varias pequeñas islas al sur de Honshu que se extienden más allá de 1800 km de Shinjuku, capital de la metrópoli y sede de la gobernación. El centro de Tokio, con sus 23 barrios, ocupa un tercio de la metrópoli, con una población cercana a los 13,23 millones de habitantes; esta área es lo que se conoce internacionalmente como la ciudad de Tokio.
