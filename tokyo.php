				<h2>Historia de tokyo</h2><br> 

				<p> <p align="justify"> A pesar de que desde tiempos antiguos existían pequeñas poblaciones y templos en las colinas cercanas a la bahía de Tokio, se considera que la fundación formal de Tokio fue en 1457, cuando un vasallo del clan Uesugi, Ōta Dōkan construyó el Castillo Edo ; así el área que rodeaba el castillo se comenzó a llamar Edo , literalmente "estuario"). El shogunato Tokugawa, que había tomado el castillo en 1590 y que tenía el control casi absoluto de Japón, estableció su gobierno en Edo en 1603, hecho que dio inicio al Período Edo en la historia japonesa. La nobleza, junto con el Emperador de Japón, permanecieron en Kioto, que siguió siendo la capital oficial, aunque sólo de manera protocolar.
				<br>
				<br>
				A partir de 1872, comenzó a construirse la primera línea de ferrocarril y entre 1885 y 1925 se construyó la Línea Yamanote, línea de ferrocarril urbano que es la más importante de Tokio en la actualidad. En 1889 se estableció la Ciudad de Tokio  con 15 barrios, luego en 1893 los distritos de Tama se unieron a la prefectura. En 1914 se inauguró la Estación de Tokio y en 1927 se inauguró el primer metro subterráneo en la Línea Ginza.
				<br>
				<br>
				El Gran terremoto de Kantō golpeó Tokio en 1923, con un saldo de aproximadamente 143 000 personas fallecidas. Tras la tragedia se inició un plan de reconstrucción que no pudo ser completado debido a su alto coste. A pesar de esto, la ciudad continuó su desarrollo hasta el comienzo de la Segunda Guerra Mundial. En 1936 se inauguró el edificio de la Kokkai (Dieta de Japón); también en ese mismo año ocurrió el Incidente del 26 de febrero, en el que 1500 oficiales del ejército japonés ocuparon el edificio de la Kokkai, el Kantei (Residencia del Primer Ministro) y otros lugares de Tokio en un intento de golpe de Estado, que fue sofocado tres días después.
				<br>
				<br>
				
				Durante la Segunda Guerra Mundial, Tokio fue intensamente bombardeada a partir de 1942 hasta 1945. A causa de esto, en 1945 la población de Tokio era la mitad que en 1940. Al terminar la guerra, en septiembre de 1945, Tokio fue ocupada militarmente y pasó a ser gobernada por las Fuerzas Aliadas. 
