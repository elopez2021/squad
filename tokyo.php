			<article> <a id="Demografia"></a>
				<h2>Demografía</h2><br>
				<p> <p align="justify">
				Tokio provee de más puestos de trabajo y lugares de recreación cultural que cualquier otra ciudad de Japón, lo que atrae a muchas personas del resto del país (especialmente jóvenes). Su densidad de población es extremadamente alta: 14 mil personas por kilómetro cuadrado, casi dos veces más que Nueva York, al ser la ciudad más poblada del mundo.
				<br>
				<br>
				El 97 % de la población de la prefectura es descendiente de japoneses. Los dos principales grupos étnicos minoritarios de Tokio son los chinos y coreanos.
				<br>
				<br>
				La religión en Tokio presenta patrones similares al resto del país, donde conviven el budismo, el sintoísmo y otras religiones. Existe un sincretismo constante, donde es frecuente que la población integre dos o más religiones a sus prácticas cotidianas. De las más de nueve mil organizaciones religiosas de la prefectura, el 38 % es budista, el 21 % es sintoísta, y el cristianismo ocupa el 13 %.13​


			
