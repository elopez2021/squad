				<h2>Vivienda de tokyo</h2><br>
				<p><p align="justify">
					La inmensa población de Tokio ha creado una altísima demanda de residencias. En el pasado, la mayoría de los habitantes de la ciudad vivía en casas de uno o dos pisos, hechas de madera, cada una con su propio jardín, patio y capilla religiosa (llamada Butsudan en los hogares budistas). A medida que la población de Tokio creció, esas casas se demolieron y en su lugar, se construyeron edificios de apartamentos. Dada la inmensa densidad de población de la región, la mayor parte de los apartamentos y casas de la ciudad son pequeñas, y están diseñadas para una familia de dos adultos y dos o tres niños.
				<br>
				<br>
				A pesar de la intensa actividad en la construcción de edificios, la demanda de residencias continuó siendo más alta que la oferta, lo que aumentó los precios del terreno y del alquiler, especialmente dentro de los 23 Barrios Especiales. Como resultado, a partir de la década de 1970, muchas personas abandonaron la región de los 23 Barrios Especiales, mudándose a Tama (parte de la prefectura de Tokio), o incluso a otras ciudades vecinas más distantes.
				<br>
				<br>
				
			
				En Tama, el Gobierno Metropolitano de Tokio creó un proyecto de viviendas baratas para familias de bajos ingresos. Sin embargo, estas residencias están localizadas muy lejos de los principales centros comerciales e industriales, y muchos de estos trabajadores pasan más de cuatro horas diarias solamente dentro de algún medio de transporte público.
				<br>
				<br>
				La ciudad de Tokio, en Japón, es uno de los puntos más densamente poblados de la región y del mundo (36 millones de habitantes en su área metropolitana) y se encuentra entre las ciudades más importantes del mundo. Tokio recibe una grande demanda de viviendas anualmente y ya que los lotes prácticamente se acabaron o son muy costosas la ciudad se erige en vertical.
